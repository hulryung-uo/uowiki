def slugify(name):
    s = name.lower()
    s = re.sub(r"[^a-z0-9]+", "-", s).strip("-")
    return s or "misc"


def md_escape(s):
    """Make a string safe inside a markdown table cell (and for Starlight)."""
    return (str(s).replace("\\", "\\\\")
                  .replace("|", "\\|")
                  .replace("<", "&lt;")
                  .replace(">", "&gt;")
                  .replace("{", "&#123;")
                  .replace("}", "&#125;")
                  .replace("\n", " ")
                  .strip())


# --------------------------------------------------------------------------
# Classifier: item -> (gameplay_category, subcategory)
# --------------------------------------------------------------------------

WEAPON_BASE_SUBCAT = {
    "BaseSword": "Swords",
    "BaseAxe": "Axes",
    "BasePoleArm": "Polearms",
    "BaseBashing": "Maces & Hammers",
    "BaseStaff": "Staves",
    "BaseKnife": "Daggers & Knives",
    "BaseSpear": "Spears & Forks",
    "BaseRanged": "Bows & Crossbows",
    "BaseThrown": "Thrown",
    "BaseWand": "Wands",
}


def _contains(name, *subs):
    low = name.lower()
    return any(s.lower() in low for s in subs)


def armor_subcat(cls):
    """First matching material substring on the class name -> subcategory."""
    checks = [
        (("Gargish",), "Gargish"),
        (("Dragon",), "Dragon Scale"),
        (("Bone",), "Bone"),
        (("Leaf", "Woodland"), "Woodland"),
        (("Studded",), "Studded"),
        (("Ring",), "Ring Mail"),
        (("Chain",), "Chain Mail"),
        (("Plate",), "Plate"),
        (("Hide", "Tiger"), "Hide"),
        (("Leather",), "Leather"),
        (("Cloth",), "Cloth"),
        (("Helm", "Bascinet", "Circlet", "Kabuto", "Jingasa", "Cowl", "Hood"),
         "Helms"),
    ]
    for subs, sub in checks:
        if _contains(cls, *subs):
            return sub
    return "Other"


def jewelry_subcat(cls):
    if _contains(cls, "Ring"):
        return "Rings"
    if _contains(cls, "Bracelet"):
        return "Bracelets"
    if _contains(cls, "Necklace", "Amulet"):
        return "Necklaces"
    if _contains(cls, "Earrings"):
        return "Earrings"
    return "Other"


def clothing_subcat(cls, bt):
    if bt == "BaseHat" or _contains(cls, "Hat", "Cap", "Bonnet", "Hood"):
        return "Hats"
    if bt == "BaseShoes" or _contains(cls, "Boots", "Sandals", "Shoes"):
        return "Footwear"
    if _contains(cls, "Robe", "Cloak", "Cape"):
        return "Robes & Cloaks"
    if _contains(cls, "Pants", "Kilt", "Skirt", "Legs", "Shorts"):
        return "Pants & Skirts"
    if _contains(cls, "Shirt", "Tunic", "Doublet", "Surcoat", "Bustier"):
        return "Shirts & Tops"
    return "Other"


def resource_subcat(bt):
    if bt == "BaseIngot":
        return "Ingots"
    if bt == "BaseOre":
        return "Ore"
    if bt in ("BaseLog", "BaseBoard"):
        return "Wood"
    if bt in ("BaseLeather", "BaseHides"):
        return "Leather & Hides"
    return "Other"


def classify(it):
    """Return (gameplay_category, subcategory) for an item.

    base_type drives the classification; the source `category` is used only
    as a fallback for items with no classifier base_type.
    """
    bt = it.get("base_type")
    cls = it["class"]
    src = it.get("category") or ""

    # WEAPONS -------------------------------------------------------------
    if bt in WEAPON_BASE_SUBCAT:
        return "Weapons", WEAPON_BASE_SUBCAT[bt]
    if bt in ("BaseMeleeWeapon", "BaseWeapon"):
        return "Weapons", "Other Weapons"

    # SHIELDS -------------------------------------------------------------
    if bt == "BaseShield":
        return "Shields", None

    # ARMOR ---------------------------------------------------------------
    if bt == "BaseArmor":
        return "Armor", armor_subcat(cls)

    # JEWELRY -------------------------------------------------------------
    if bt == "BaseJewel":
        return "Jewelry", jewelry_subcat(cls)

    # SPELLCASTING --------------------------------------------------------
    if bt == "Spellbook":
        return "Spellbooks & Talismans", "Spellbooks"
    if bt == "Runebook":
        return "Spellbooks & Talismans", "Runebooks"
    if bt == "BaseTalisman":
        return "Spellbooks & Talismans", "Talismans"

    # INSTRUMENTS ---------------------------------------------------------
    if bt == "BaseInstrument":
        return "Instruments", None

    # CLOTHING ------------------------------------------------------------
    if bt in ("BaseClothing", "BaseHat", "BaseShoes"):
        return "Clothing", clothing_subcat(cls, bt)

    # POTIONS -------------------------------------------------------------
    if bt == "BasePotion":
        return "Potions", None

    # FOOD & DRINK --------------------------------------------------------
    if bt in ("Food", "BaseBeverage"):
        return "Food & Drink", None

    # REAGENTS ------------------------------------------------------------
    if bt == "BaseReagent":
        return "Reagents", None

    # RESOURCES (by base_type) -------------------------------------------
    if bt in ("BaseIngot", "BaseOre", "BaseLog", "BaseBoard",
              "BaseLeather", "BaseHides"):
        return "Resources", resource_subcat(bt)

    # SCROLLS -------------------------------------------------------------
    if cls.endswith("Scroll") or "Scroll" in cls:
        return "Scrolls", None

    # TOOLS ---------------------------------------------------------------
    if bt == "BaseTool" or src == "Tools":
        return "Tools", None

    # CONTAINERS ----------------------------------------------------------
    if bt == "BaseContainer" or src == "Containers":
        return "Containers", None

    # LIGHTING ------------------------------------------------------------
    if bt == "BaseLight" or _contains(cls, "Lantern", "Torch", "Candle", "Lamp"):
        return "Lighting", None

    # BOOKS ---------------------------------------------------------------
    if bt == "BaseBook" or src == "Books":
        return "Books", None

    # RESOURCES (source fallback) ----------------------------------------
    if src == "Resource":
        return "Resources", resource_subcat(bt)

    # FALLBACK by source category ----------------------------------------
    fallback = {
        "Decorative": ("Decorations", None),
        "Addons": ("Addons & Furniture", None),
        "Quest": ("Quest Items", None),
        "Artifacts": ("Artifacts", None),
        "Functional": ("Functional", None),
        "StoreBought": ("Miscellaneous", None),
    }
    if src in fallback:
        return fallback[src]
    return "Miscellaneous", None


