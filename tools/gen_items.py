EXPANSION_PATH = os.path.join(ROOT, "data", "item_expansion.json")
DOCS_ROOT = os.path.join(ROOT, "src", "content", "docs")


def out_dir(locale):
    base = DOCS_ROOT if locale == "en" else os.path.join(DOCS_ROOT, locale)
    return os.path.join(base, "items", "catalog")


def cat_label(locale, category):
    """Localized gameplay-category name (English fallback via the key system)."""
    return L(locale, "cat." + category)


def cat_blurb(locale, category):
    return L(locale, "blurb." + category)


# Subcategories that have a glossary/generic translation; weapon families and
# other proper-noun-ish material names stay English (no label key → English).
def sub_label(locale, sub):
    if sub is None:
        return None
    try:
        return L(locale, "isub." + sub)
    except KeyError:
        return sub

