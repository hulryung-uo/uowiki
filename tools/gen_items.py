#!/usr/bin/env python3
"""Generate the item catalog under src/content/docs/items/catalog/ from
data/items.json, classified by GAMEPLAY TYPE.

Items are reclassified from their ServUO source directory into gameplay
categories (Weapons by family, Armor by material, Shields, Jewelry,
Clothing, etc.) using the `base_type` field (the first classifier base
class in each item's ancestor chain) first, and the source `category` as a
fallback. So an Artifact katana lands in Weapons > Swords, not Artifacts.

Produces:
  catalog/index.md         — overview table of gameplay categories + links
  catalog/<slug>.md        — one page per gameplay category, grouped by
                             subcategory, each group an
                             Icon | Name | Item ID | Weight table.

All generated pages carry `generated: true` and the AUTO-GENERATED banner;
never hand-edit them — fix tools/extract_items.py / gen_items.py or the
source data and regenerate. Every banner-marked file in catalog/ is deleted
before regeneration so no stale source-dir pages remain.

Usage: python3 tools/gen_items.py
"""

