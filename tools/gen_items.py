def frontmatter(title, description, sources, order):
    lines = [
        "---",
        "title: %s" % json.dumps(title),
        "description: %s" % json.dumps(description),
        "status: source-verified",
        "generated: true",
        "sidebar:",
        "  order: %d" % order,
        "sources:",
    ]
    lines += ['  - "%s"' % s for s in sources]
    lines += ["last_verified: %s" % TODAY, "---", "", BANNER, ""]
    return "\n".join(lines)


def icon_cell(png):
    if not png:
        return ""
    return ('<img src="%s" class="uo-sprite" alt="" width="48" '
            'loading="lazy" />' % png)


def weight_cell(weight):
    if weight is None:
        return "—"
    if isinstance(weight, float) and weight == int(weight):
        weight = int(weight)
    return str(weight)


def clean_generated(out_dir):
    """Delete every banner-marked .md file in out_dir (incl. old source-dir
    pages) so no stale catalog pages remain."""
    if not os.path.isdir(out_dir):
        return 0
    removed = 0
    for fn in os.listdir(out_dir):
        if not fn.endswith(".md"):
            continue
        p = os.path.join(out_dir, fn)
        try:
            text = open(p, encoding="utf-8").read()
        except OSError:
            continue
        if "AUTO-GENERATED" in text:
            os.remove(p)
            removed += 1
    return removed


def subcat_sort_key(category):
    """Ordering for subcategory headings within a category page."""
    if category == "Weapons":
        order = list(WEAPON_SUBCATS.keys())
        return lambda s: (order.index(s) if s in order else len(order), s.lower())
    if category == "Armor":
        order = ["Cloth", "Leather", "Studded", "Hide", "Bone", "Ring Mail",
                 "Chain Mail", "Plate", "Dragon Scale", "Woodland", "Gargish",
                 "Helms", "Other"]
        return lambda s: (order.index(s) if s in order else len(order), s.lower())
    if category == "Resources":
        order = ["Ingots", "Ore", "Wood", "Leather & Hides", "Other"]
        return lambda s: (order.index(s) if s in order else len(order), s.lower())
    return lambda s: (0, s.lower())


def heading_for(category, sub, locale):
    """Subcategory ## heading text (weapons show their training skill).

    Subcategory name is localized where a glossary/generic term exists, else
    kept English (weapon families, armor materials). The weapon training skill
    uses its official localized name."""
    label = sub_label(locale, sub)
    if category == "Weapons":
        skill = WEAPON_SUBCATS.get(sub)
        if skill:
            return "%s  ·  %s" % (md_escape(label), md_escape(SKILL(locale, skill)))
    return md_escape(label)


# Expansion timeline — items are grouped under these in chronological order, so
# Classic items come first, then each later expansion. Untagged items (era not
# determinable from the source) fall into a final section.
ERA_ORDER = ["Classic", "AOS", "SE", "ML", "SA", "HS", "TOL", "EJ"]
ERA_NAMES = {
    "Classic": "Classic (The Second Age era)",
    "AOS": "Age of Shadows (AOS)",
    "SE": "Samurai Empire (SE)",
    "ML": "Mondain's Legacy (ML)",
    "SA": "Stygian Abyss (SA)",
    "HS": "High Seas (HS)",
    "TOL": "Time of Legends (TOL)",
    "EJ": "Endless Journey (EJ)",
}
ERA_UNTAGGED = "Era not determined"


def _table(out, rows, locale):
    out.append("| %s | %s | %s | %s |" % (
        L(locale, "col.icon"), L(locale, "col.name"),
        L(locale, "col.item_id"), L(locale, "col.weight")))
    out.append("|------|------|---------|--------|")
    for it in rows:
        # Item names are proper nouns — kept English. ID/weight unchanged.
        out.append("| %s | %s | `%s` | %s |" % (
            icon_cell(it["png"]), md_escape(it["name"]),
            it["item_id"], weight_cell(it["weight"])))
    out.append("")


def _write_subcats(out, category, rows, heading_level, locale):
    """Emit rows grouped by subcategory under the given heading level (## or ###)."""
    groups = {}
    for it in rows:
        groups.setdefault(it["subcategory"] or "All", []).append(it)
    keys = sorted(groups.keys(), key=subcat_sort_key(category))
    single = len(keys) == 1 and keys[0] == "All"
    for sub in keys:
        if not single:
            out.append("%s %s\n" % (heading_level, heading_for(category, sub, locale)))
        _table(out, groups[sub], locale)


def write_category_page(out_dir_path, category, items, locale):
    slug = slugify(category)
    order = CATEGORY_ORDER[category]

    blurb = cat_blurb(locale, category)
    cat_name = cat_label(locale, category)
    title = cat_name
    description = L(locale, "items.cat_desc",
                   category=cat_name, n=len(items), blurb=blurb)
    sources = ["servuo: Scripts/Items", "servuo: Scripts/Services/Craft (eras)",
               "client: artLegacyMUL.uop"]

    out = [frontmatter(title, description, sources, order)]
    out.append(L(locale, "items.cat_intro",
                 category=cat_name, blurb=blurb, n=len(items)) + "\n")

    # Bucket by era; only craftable items carry one, the rest go to "untagged".
    by_era = {}
    for it in items:
        by_era.setdefault(it.get("era") or ERA_UNTAGGED, []).append(it)

    untagged_label = L(locale, "items.era_untagged")
    has_any_era = any(e in by_era for e in ERA_ORDER)
    if not has_any_era:
        # Nothing in this category is era-tagged — just group by subcategory.
        _write_subcats(out, category, items, "##", locale)
    else:
        # Era headings keep the English expansion label (proper-noun abbrev).
        for era in ERA_ORDER:
            if era not in by_era:
                continue
            rows = by_era[era]
            out.append("## %s  *(%d)*\n" % (md_escape(ERA_NAMES[era]), len(rows)))
            _write_subcats(out, category, rows, "###", locale)
        if ERA_UNTAGGED in by_era:
            rows = by_era[ERA_UNTAGGED]
            out.append("## %s  *(%d)*\n" % (md_escape(untagged_label), len(rows)))
            out.append(L(locale, "items.untagged_note") + "\n")
            _write_subcats(out, category, rows, "###", locale)

    with open(os.path.join(out_dir_path, "%s.md" % slug), "w") as f:
        f.write("\n".join(out).rstrip() + "\n")
    return slug


def write_index(out_dir_path, ordered_cats, cat_slugs, counts, total, locale):
    pfx = locale_prefix(locale)
    title = L(locale, "items.title")
    description = L(locale, "items.index_desc")
    sources = ["servuo: Scripts/Items", "client: artLegacyMUL.uop"]
    out = [frontmatter(title, description, sources, 0)]
    out.append(L(locale, "items.index_intro",
                 total=total, ncat=len(ordered_cats)) + "\n")
    out.append(L(locale, "items.index_regen") + "\n")
    out.append("| %s | %s |" % (L(locale, "col.category"), L(locale, "col.items")))
    out.append("|----------|-------|")
    for cat in ordered_cats:
        out.append("| [%s](%s/items/catalog/%s/) | %d |"
                   % (md_escape(cat_label(locale, cat)), pfx, cat_slugs[cat], counts[cat]))
    out.append("| **%s** | **%d** |" % (L(locale, "col.total"), total))
    out.append("")
    with open(os.path.join(out_dir_path, "index.md"), "w") as f:
        f.write("\n".join(out).rstrip() + "\n")


def main():
    doc = json.load(open(DATA_PATH))
    items = doc["items"]

    # Overlay expansion-introduction era (by item class) from
    # item_expansion.json. Only craftable classes are present there; items
    # with no entry simply have no era (blank cell / no Era column).
    expansion = json.load(open(EXPANSION_PATH)).get("by_class", {})
    for it in items:
        era = expansion.get(it.get("class"))
        if era:
            it["era"] = era

    # Classify every item into a gameplay (category, subcategory).
    cat_items = {}
    for it in items:
        cat, sub = classify(it)
        it = dict(it)
        it["subcategory"] = sub
        cat_items.setdefault(cat, []).append(it)

    # Sort items within each category by subcategory then name.
    for cat, rows in cat_items.items():
        rows.sort(key=lambda x: ((x["subcategory"] or "").lower(),
                                 x["name"].lower(), x["class"].lower()))

    # Emit pages in defined sidebar order; skip empty categories.
    ordered_cats = [c for c in CATEGORY_ORDER if cat_items.get(c)]

    for locale in LOCALES:
        out = out_dir(locale)
        os.makedirs(out, exist_ok=True)
        removed = clean_generated(out)
        if removed:
            print("[%s] removed %d stale generated page(s)" % (locale, removed))

        cat_slugs = {}
        counts = {}
        for cat in ordered_cats:
            rows = cat_items[cat]
            slug = write_category_page(out, cat, rows, locale)
            cat_slugs[cat] = slug
            counts[cat] = len(rows)

        write_index(out, ordered_cats, cat_slugs, counts, len(items), locale)
        print("[%s] wrote catalog/index.md + %d category pages (%d items total)"
              % (locale, len(cat_slugs), len(items)))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
