import json
import os
import re
from collections import OrderedDict

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DATA_PATH = os.path.join(ROOT, "data", "items.json")
EXPANSION_PATH = os.path.join(ROOT, "data", "item_expansion.json")
OUT_DIR = os.path.join(ROOT, "src", "content", "docs", "items", "catalog")

BANNER = ("<!-- AUTO-GENERATED by tools/gen_items.py from data/items.json"
          " — do not hand-edit -->")
TODAY = datetime.date.today().isoformat()

