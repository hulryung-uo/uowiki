# Page rendering
# --------------------------------------------------------------------------

def frontmatter(title, description, sources, order):
    lines = [
        "---",
        "title: %s" % json.dumps(title),
        "description: %s" % json.dumps(description),
        "status: source-verified",
        "generated: true",
        "sidebar:",
        "  order: %d" % order,
        "sources:",
    ]
    lines += ['  - "%s"' % s for s in sources]
    lines += ["last_verified: %s" % TODAY, "---", "", BANNER, ""]
    return "\n".join(lines)


def icon_cell(png):
    if not png:
        return ""
    return ('<img src="%s" class="uo-sprite" alt="" width="48" '
            'loading="lazy" />' % png)


def weight_cell(weight):
    if weight is None:
        return "—"
    if isinstance(weight, float) and weight == int(weight):
        weight = int(weight)
    return str(weight)


def clean_generated(out_dir):
    """Delete every banner-marked .md file in out_dir (incl. old source-dir
    pages) so no stale catalog pages remain."""
    if not os.path.isdir(out_dir):
        return 0
    removed = 0
    for fn in os.listdir(out_dir):
        if not fn.endswith(".md"):
            continue
        p = os.path.join(out_dir, fn)
        try:
            text = open(p, encoding="utf-8").read()
        except OSError:
            continue
        if "AUTO-GENERATED" in text:
            os.remove(p)
            removed += 1
    return removed


def subcat_sort_key(category):
    """Ordering for subcategory headings within a category page."""
    if category == "Weapons":
        order = list(WEAPON_SUBCATS.keys())
        return lambda s: (order.index(s) if s in order else len(order), s.lower())
    if category == "Armor":
        order = ["Cloth", "Leather", "Studded", "Hide", "Bone", "Ring Mail",
                 "Chain Mail", "Plate", "Dragon Scale", "Woodland", "Gargish",
                 "Helms", "Other"]
        return lambda s: (order.index(s) if s in order else len(order), s.lower())
    if category == "Resources":
        order = ["Ingots", "Ore", "Wood", "Leather & Hides", "Other"]
        return lambda s: (order.index(s) if s in order else len(order), s.lower())
    return lambda s: (0, s.lower())


def heading_for(category, sub):
    """Subcategory ## heading text (weapons show their training skill)."""
    if category == "Weapons":
        skill = WEAPON_SUBCATS.get(sub)
        if skill:
            return "%s  ·  %s" % (md_escape(sub), md_escape(skill))
    return md_escape(sub)


def write_category_page(out_dir, category, items):
    slug = slugify(category)
    order = CATEGORY_ORDER[category]

    # group by subcategory
    groups = {}
    for it in items:
        sub = it["subcategory"] or "All"
        groups.setdefault(sub, []).append(it)
    sub_keys = sorted(groups.keys(), key=subcat_sort_key(category))

    # Include an Era column on this page only if at least one item has a
    # known expansion era. Kept consistent across all subcategory tables.
    show_era = any(it.get("era") for it in items)

    blurb = CATEGORY_BLURB.get(category, "items")
    title = category
    description = ("Every %s item in the ServUO source (%d) — %s — with art, "
                   "item IDs and weights." % (category, len(items), blurb))
    sources = ["servuo: Scripts/Items", "client: artLegacyMUL.uop"]

    out = [frontmatter(title, description, sources, order)]
    out.append("Auto-generated catalog of every %s — %s (%d items). Items are "
               "classified by gameplay type from the ServUO source; icons are "
               "static client art and item IDs are the resolved ServUO "
               "`ItemID`.\n" % (category, blurb, len(items)))

    single = len(sub_keys) == 1 and sub_keys[0] == "All"
    for sub in sub_keys:
        rows = groups[sub]
        if not single:
            out.append("## %s\n" % heading_for(category, sub))
        if show_era:
            out.append("| Icon | Name | Item ID | Weight | Era |")
            out.append("|------|------|---------|--------|-----|")
            for it in rows:
                out.append("| %s | %s | `%s` | %s | %s |" % (
                    icon_cell(it["png"]),
                    md_escape(it["name"]),
                    it["item_id"],
                    weight_cell(it["weight"]),
                    it.get("era") or "",
                ))
        else:
            out.append("| Icon | Name | Item ID | Weight |")
            out.append("|------|------|---------|--------|")
            for it in rows:
                out.append("| %s | %s | `%s` | %s |" % (
                    icon_cell(it["png"]),
                    md_escape(it["name"]),
                    it["item_id"],
                    weight_cell(it["weight"]),
                ))
        out.append("")

    with open(os.path.join(out_dir, "%s.md" % slug), "w") as f:
        f.write("\n".join(out).rstrip() + "\n")
    return slug


def write_index(out_dir, ordered_cats, cat_slugs, counts, total):
    title = "Item Catalog"
    description = ("Image-rich catalog of every item in the ServUO source, "
                   "grouped by gameplay type.")
    sources = ["servuo: Scripts/Items", "client: artLegacyMUL.uop"]
    out = [frontmatter(title, description, sources, 0)]
    out.append("A complete, auto-generated catalog of every concrete item "
               "class in the ServUO source — %d items across %d gameplay "
               "categories. Items are classified by gameplay type (weapons by "
               "family, armor by material, shields, jewelry, clothing, and so "
               "on) rather than by source directory. Each category page lists "
               "items with their client art, item ID and weight, grouped by "
               "subcategory.\n" % (total, len(ordered_cats)))
    out.append("This catalog is regenerated from `data/items.json` by "
               "`tools/gen_items.py`; do not hand-edit the category pages.\n")
    out.append("| Category | Items |")
    out.append("|----------|-------|")
    for cat in ordered_cats:
        out.append("| [%s](/items/catalog/%s/) | %d |"
                   % (md_escape(cat), cat_slugs[cat], counts[cat]))
    out.append("| **Total** | **%d** |" % total)
    out.append("")
    with open(os.path.join(out_dir, "index.md"), "w") as f:
        f.write("\n".join(out).rstrip() + "\n")


def main():
    doc = json.load(open(DATA_PATH))
    items = doc["items"]

    # Overlay expansion-introduction era (by item class) from
    # item_expansion.json. Only craftable classes are present there; items
    # with no entry simply have no era (blank cell / no Era column).
    expansion = json.load(open(EXPANSION_PATH)).get("by_class", {})
    for it in items:
        era = expansion.get(it.get("class"))
        if era:
            it["era"] = era

    os.makedirs(OUT_DIR, exist_ok=True)
    removed = clean_generated(OUT_DIR)
    if removed:
        print("removed %d stale generated page(s)" % removed)

    # Classify every item into a gameplay (category, subcategory).
    cat_items = {}
    for it in items:
        cat, sub = classify(it)
        it = dict(it)
        it["subcategory"] = sub
        cat_items.setdefault(cat, []).append(it)

    # Sort items within each category by subcategory then name.
    for cat, rows in cat_items.items():
        rows.sort(key=lambda x: ((x["subcategory"] or "").lower(),
                                 x["name"].lower(), x["class"].lower()))

    # Emit pages in defined sidebar order; skip empty categories.
    ordered_cats = [c for c in CATEGORY_ORDER if cat_items.get(c)]
    cat_slugs = {}
    counts = {}
    for cat in ordered_cats:
        rows = cat_items[cat]
        slug = write_category_page(OUT_DIR, cat, rows)
        cat_slugs[cat] = slug
        counts[cat] = len(rows)
        print("  %-24s -> catalog/%s.md (%d items)" % (cat, slug, len(rows)))

    write_index(OUT_DIR, ordered_cats, cat_slugs, counts, len(items))

    print("\nwrote catalog/index.md + %d category pages (%d items total)"
          % (len(cat_slugs), len(items)))
    print("sidebar order:")
    for cat in ordered_cats:
        print("  %2d  %-24s catalog/%s.md"
              % (CATEGORY_ORDER[cat], cat, cat_slugs[cat]))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
