DRESSED = {}


def _creature_art(c, anim_data):
    """Resolve (gif, png, dressed) art paths for a creature.

    Prefers the dressed (clothed/armed) render when one exists; otherwise falls
    back to the bare-body GIF/PNG from creature_anim.json. Returns (gif, png,
    is_dressed) with None entries when no art exists."""
    cls = c["class"]
    dressed = DRESSED.get(cls)
    if dressed and dressed.get("gif"):
        return dressed.get("gif"), dressed.get("png"), True
    if anim_data and cls in anim_data.get("creatures", {}):
        body_id = anim_data["creatures"][cls]
        body = anim_data.get("bodies", {}).get(str(body_id))
        if body:
            return body.get("gif"), body.get("png"), False
    return None, None, False

