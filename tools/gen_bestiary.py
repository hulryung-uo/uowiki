        "status: source-verified",
        "generated: true",
        "sources:",
        '  - "servuo: Scripts/Mobiles/ (via tools/extract_creatures.py)"',
        '  - "client: anim*.mul (via tools/extract_anim.py)"',
        f"last_verified: {LAST_VERIFIED}",
